const connectDB = async () => {
  try {
    await mongoose.connect(config.DB_URL + `/${config.DB_NAME}`);
    console.log("\x1b[32m%s\x1b[0m", "\n💾  Connected to MongoDB");
  } catch (error) {
    console.log("\x1b[31m%s\x1b[0m", "Error connecting to MongoDB:", error);
    process.exit(1);
  }
};

connectDB().then(() => {
  mockQueues.forEach(async (queue) => {
    await Queue.create(queue).catch((err) => {
      console.error(`Error creating queue ${queue.queueId}:`, err);
    });
  });

  mockTokens.forEach(async (token) => {
    await Token.create(token).catch((err) => {
      console.error(`Error creating token ${token.token}:`, err);
    });
  });
});
